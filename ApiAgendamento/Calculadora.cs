
namespace ApiAgendamento
{
    public class Calculadora
    {
        public int Somar(int a, int b)
        {
            return a + b;
        }

        public bool EhPar(int numero)
        {
            return numero % 2 == 0;
        }

        public int Subtrair(int a, int b)
        {
            return a - b;
        }
    }
}
